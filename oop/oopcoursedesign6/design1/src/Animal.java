public abstract class Animal {
    private String name;
    private int age;

    public Animal(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public abstract void playing();

    public abstract void strolling();

    public abstract void breaking();

    public abstract void happy();

    public abstract void showIform();

    public abstract void eat(Food food,int a);

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
